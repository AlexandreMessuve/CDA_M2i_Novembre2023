let nombre = 1;

//boucle for
console.log("Début de la boucle for : \n");

for (let i = 1; i <= 10; i++) {
    console.log(i + "\n");
    
}
console.log("Fin de la boucle for : \n");

//boucle while 

console.log("Début de la boucle while : \n");

while (nombre <= 10) {
    console.log(nombre + "\n");
    nombre++
}

console.log("Fin de la boucle while : \n");